package org.openo.sdno.brs.model;

import org.openo.sdno.brs.validator.rules.StrRule;

/**
 * Resource module.<br>
 * 
 * @author
 * @version SDNO 0.5 2016-5-19
 */

public abstract class Resource extends RootEntity {

    @StrRule(range = "0-64", paramName = "version")
    protected String version;

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((version == null) ? 0 : version.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if(this == obj) {
            return true;
        }
        if(obj == null) {
            return false;
        }
        if(getClass() != obj.getClass()) {
            return false;
        }
        Resource other = (Resource)obj;
        if(version == null) {
            if(other.version != null) {
                return false;
            }
        } else if(!version.equals(other.version)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Resource [version=" + version + ", id=" + id + ", description=" + description + "]";
    }

}
